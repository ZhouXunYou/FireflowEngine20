package org.fireflow.demo.biztrip.entity;

import java.util.Date;

/**
 * 出差申请信息表
 * @author 非也 nychen2000@163.com
 * Fire Workflow 官方网站：www.firesoa.com 或者 www.fireflow.org
 *
 */
public class BusinessTripBasicInfo {
	String id = null;
	String bizId = null;//单据编号
	String applicantId = null;//申请人Id
	String applicantName = null;//申请人姓名
	String departmentName = null;//所在部门
	String positionName = null;//职务名称
	String phoneNumber = null;//联系电话
	
	String destinationCity = null;//出差地点
	String startDate = null;//开始时间
	String endDate = null;//结束时间
	int totalDays = 0;//总天数
	
	String subject = null;//事由
	
	Date lastUpdateTime = null;

	public Date getLastUpdateTime() {
		return lastUpdateTime;
	}

	public void setLastUpdateTime(Date lastUpdateTime) {
		this.lastUpdateTime = lastUpdateTime;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getBizId() {
		return bizId;
	}

	public void setBizId(String biz_Id) {
		this.bizId = biz_Id;
	}

	public String getApplicantId() {
		return applicantId;
	}

	public void setApplicantId(String applicant) {
		this.applicantId = applicant;
	}

	public String getApplicantName() {
		return applicantName;
	}

	public void setApplicantName(String applicantName) {
		this.applicantName = applicantName;
	}

	public String getDepartmentName() {
		return departmentName;
	}

	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}

	public String getPositionName() {
		return positionName;
	}

	public void setPositionName(String positionName) {
		this.positionName = positionName;
	}

	public String getPhoneNumber() {
		return phoneNumber;
	}

	public void setPhoneNumber(String telNumber) {
		this.phoneNumber = telNumber;
	}

	public String getDestinationCity() {
		return destinationCity;
	}

	public void setDestinationCity(String destinationCity) {
		this.destinationCity = destinationCity;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public int getTotalDays() {
		return totalDays;
	}

	public void setTotalDays(int totalDays) {
		this.totalDays = totalDays;
	}
}
